// Thumbnails will be created the first time the script is run.
//
// TO USE
// -----------------------
// 1) Place this file in the same folder as the SimpleViewer SWF.
// 2) Set your Image Album attributes below (maxImageDimension, title etc).
// 3) Set the xmlDataPath in index.html:
// in the object tag:
// <param name=FlashVars value="xmlDataPath=imageData.php">
// and in the embed tag:
// FlashVars="xmlDataPath=imageData.php"/> 
//

$xml = '<?xml version="1.0" encoding="UTF-8" ?>';
$xml .= '<SIMPLEVIEWER_DATA maxImageDimension="680" textColor="0xFFFFFF" frameColor="0xFFFFFF" frameWidth="2" stagePadding="5" thumbnailColumns="2" thumbnailRows="7" navPosition="left" navDirection="RTL" title="Illustration Gallery" imagePath="" thumbPath="">';

$count = "1";
$style = opendir("images");

	while($stylesheet = readdir($style)) {
		if ($stylesheet[0] != "." && $stylesheet[0] != ".." ) {
			$xml .= '<IMAGE>';
			$xml .= '<NAME>'.$stylesheet.'</NAME>';
			//add auto captions: 'Image X'
			$xml .= '<CAPTION>'.$stylesheet.'</CAPTION>';
			$xml .= '</IMAGE>';
			if (!file_exists("./thumbs/".$stylesheet))
			{
			$thumb=new thumbnail("./images/".$stylesheet);	
			$thumb->size_auto(45);
			$thumb->save("./thumbs/".$stylesheet);
			}
		$count++;
		}
	}
closedir($style);
$xml .= '</SIMPLEVIEWER_DATA>';
echo $xml;


